package gr.uoa.di.thanos.botcraft.geometry;

/**
 * Enumeration representing 4 x 4 matrix components (both rows and columns).
 * 
 * @author thanos
 */
public enum MatrixComponent {
	/**
	 * The x row or column.
	 */
	X,

	/**
	 * The y row or column.
	 */
	Y,

	/**
	 * The z row or column.
	 */
	Z,

	/**
	 * The w row or column.
	 */
	W
}
